##Types of questions
##Descriptive analysis -- to describe a set of data  e.g census/google ngrams viewer
##Exploratory analysis -- discover new possible connection, correlation does not imply causation
##Inferential analysis -- use small sample data to say something about bigger population, esimation, uncertainty, quantify
##Predictive  analysis -- use some data to predict other objects, X predicts Y does not mean X causes Y
##Causal analysis      -- to find out what happens to one vaiable if another changes, usually use randomized studies
##Mechanistic analysis -- rare. understand exact changes in variable that lead to changes in other variables
